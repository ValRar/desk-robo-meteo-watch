void get_datetime(char *date_buf, size_t date_size, char *time_buf, size_t time_size)
{
  tm time_info;
  getLocalTime(&time_info);
  strftime(date_buf, date_size, "%d %B", &time_info);
  strftime(time_buf, time_size, "%02H:%02M", &time_info);
}

